#include <string.h>
#include <stdio.h>

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t				i;
	const unsigned char	*str1;
	const unsigned char	*str2;

	i = 0;
	str1 = (const unsigned char *)s1;
	str2 = (const unsigned char *)s2;
	while (i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}

int		main(void)
{
	printf("ft_memcmp: %d\n", ft_memcmp("Hayo", "Hayo", 4));
	printf("   memcmp: %d\n", memcmp("Hayo", "Hayo", 4));
	return (0);
}
